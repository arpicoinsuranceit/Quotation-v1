package org.arpicoinsurance.groupit.main.helper;

public class MedicalRequirementsHelper {

	private String medicalReqname;
	private String mainStatus;
	private String spouseStatus;
	
	public MedicalRequirementsHelper() {
		super();
	}

	public String getMedicalReqname() {
		return medicalReqname;
	}

	public void setMedicalReqname(String medicalReqname) {
		this.medicalReqname = medicalReqname;
	}

	public String getMainStatus() {
		return mainStatus;
	}

	public void setMainStatus(String mainStatus) {
		this.mainStatus = mainStatus;
	}

	public String getSpouseStatus() {
		return spouseStatus;
	}

	public void setSpouseStatus(String spouseStatus) {
		this.spouseStatus = spouseStatus;
	}

	
}
