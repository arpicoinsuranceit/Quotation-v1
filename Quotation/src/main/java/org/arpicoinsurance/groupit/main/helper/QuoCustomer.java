package org.arpicoinsurance.groupit.main.helper;

public class QuoCustomer {
	private String mainLifeName;
	private Integer mainLifeAge;
	private String mainLifeOccupation;
	private String spouseName;
	private Integer spouseAge;
	private String spouseOccupation;
	private Integer term;
	private String mode;
	private Double modePremium;
	
	public String getMainLifeName() {
		return mainLifeName;
	}
	public void setMainLifeName(String mainLifeName) {
		this.mainLifeName = mainLifeName;
	}
	public Integer getMainLifeAge() {
		return mainLifeAge;
	}
	public void setMainLifeAge(Integer mainLifeAge) {
		this.mainLifeAge = mainLifeAge;
	}
	public String getMainLifeOccupation() {
		return mainLifeOccupation;
	}
	public void setMainLifeOccupation(String mainLifeOccupation) {
		this.mainLifeOccupation = mainLifeOccupation;
	}
	public String getSpouseName() {
		return spouseName;
	}
	public void setSpouseName(String spouseName) {
		this.spouseName = spouseName;
	}
	public Integer getSpouseAge() {
		return spouseAge;
	}
	public void setSpouseAge(Integer spouseAge) {
		this.spouseAge = spouseAge;
	}
	public String getSpouseOccupation() {
		return spouseOccupation;
	}
	public void setSpouseOccupation(String spouseOccupation) {
		this.spouseOccupation = spouseOccupation;
	}
	public Integer getTerm() {
		return term;
	}
	public void setTerm(Integer term) {
		this.term = term;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public Double getModePremium() {
		return modePremium;
	}
	public void setModePremium(Double modePremium) {
		this.modePremium = modePremium;
	}
	
	
}
