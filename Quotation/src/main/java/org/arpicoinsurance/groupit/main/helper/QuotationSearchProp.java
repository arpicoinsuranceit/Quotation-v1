package org.arpicoinsurance.groupit.main.helper;

public class QuotationSearchProp {
	private String QuotationId;
	private String QuotationDetailId;
	private Integer SeqId;
	
	public String getQuotationId() {
		return QuotationId;
	}
	public void setQuotationId(String quotationId) {
		QuotationId = quotationId;
	}
	public String getQuotationDetailId() {
		return QuotationDetailId;
	}
	public void setQuotationDetailId(String quotationDetailId) {
		QuotationDetailId = quotationDetailId;
	}
	public Integer getSeqId() {
		return SeqId;
	}
	public void setSeqId(Integer seqId) {
		SeqId = seqId;
	}
}
